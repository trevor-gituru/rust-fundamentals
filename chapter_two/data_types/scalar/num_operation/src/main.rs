/*
 * The following program shows basic numerical operations that can be performed with rust
 */
fn main() {
    //Addition
    let sum = 5+8;
    //Subtraction
    let diff = 2.7-6.7;
    //Multiplication
    let mult = 12*5;
    // Division
    let div = -5/3;
    //Remainder
    let rem = 43%5;
    println!("Sum: {}\nDifference : {}\nMultiplication : {}\nDivision : {}\nRemainder: {}\n", sum, diff, mult, div, rem);
}
